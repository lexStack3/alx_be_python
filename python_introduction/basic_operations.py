#!/usr/bin/python3
"""Basic arithmetic operations in Python by performing
predefined calculations.
"""
number1 = 10
number2 = 5
print("Addition of {} and {} is {}".format(number1,
                                           number2,
                                           number1 + number2))
print("Substraction of {} and {} is {}".format(number2,
                                               number1,
                                               number1 - number2))
print("Multiplication of {} and {} is {}".format(number1,
                                                 number2,
                                                 number1 * number1))
